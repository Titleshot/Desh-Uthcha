import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");

function unescapeJsString(str) {
  return str
    .replace(/\\n/g, " ")
    .replace(/\\"/g, '"')
    .replace(/\\'/g, "'")
    .replace(/\s+/g, " ")
    .trim();
}

const blockRe =
  /pointNumber:\s*(\d+),[\s\S]*?promise:\s*\n\s*"([\s\S]*?)"\s*,\s*\n\s*promiseNe:\s*\n\s*"([\s\S]*?)"\s*,/g;

function extractFromSource(text) {
  const map = new Map();
  let m;
  while ((m = blockRe.exec(text)) !== null) {
    const n = parseInt(m[1], 10);
    map.set(n, {
      en: unescapeJsString(m[2]),
      ne: unescapeJsString(m[3]),
    });
  }
  return map;
}

function hasDevanagari(s) {
  return /[\u0900-\u097F]/.test(s.slice(0, Math.min(120, s.length)));
}

function summarizeEn(t, maxLen = 300) {
  if (t.length <= maxLen) return t;
  const sentences = t.split(/(?<=[.!?])\s+/);
  let acc = "";
  for (const sent of sentences) {
    const next = acc ? `${acc} ${sent}` : sent;
    if (next.length > maxLen) break;
    acc = next;
  }
  if (acc.length >= 70 && acc.length < t.length - 15) return acc;
  let cut = t.slice(0, maxLen);
  const lastSpace = cut.lastIndexOf(" ");
  if (lastSpace > maxLen * 0.55) cut = cut.slice(0, lastSpace);
  return `${cut.trim()}…`;
}

function summarizeNe(t, maxLen = 300) {
  if (t.length <= maxLen) return t;
  const parts = t.split(/\s*।\s*/).filter(Boolean);
  if (parts.length > 1) {
    let acc = "";
    for (const p of parts) {
      const piece = p.trim();
      const next = acc ? `${acc}। ${piece}` : piece;
      if (next.length > maxLen) break;
      acc = next;
    }
    if (acc.length >= 70 && acc.length < t.length - 15) {
      return acc.endsWith("।") ? acc : `${acc}।`;
    }
  }
  let cut = t.slice(0, maxLen);
  const lastSpace = cut.lastIndexOf(" ");
  if (lastSpace > maxLen * 0.55) cut = cut.slice(0, lastSpace);
  return `${cut.trim()}…`;
}

function summarize(text, maxLen) {
  const t = text.replace(/\s+/g, " ").trim();
  if (!t) return t;
  return hasDevanagari(t) ? summarizeNe(t, maxLen) : summarizeEn(t, maxLen);
}

const appPath = path.join(root, "src", "App.jsx");
const appText = fs.readFileSync(appPath, "utf8");
const fromApp = extractFromSource(appText);

const cab71 = fs.readFileSync(path.join(root, "src", "cabinetPoints71to85.js"), "utf8");
const cab86 = fs.readFileSync(path.join(root, "src", "cabinetPoints86to100.js"), "utf8");
for (const [n, v] of extractFromSource(cab71)) fromApp.set(n, v);
for (const [n, v] of extractFromSource(cab86)) fromApp.set(n, v);

const lines = [
  "/** Auto-generated by scripts/build-promise-summaries.mjs — short leads for scanning; full text stays on each point. */",
  "",
  "export const promiseSummaries = {",
];

for (let i = 1; i <= 100; i++) {
  const row = fromApp.get(i);
  if (!row) {
    console.error(`Missing point ${i}`);
    process.exit(1);
  }
  const summary = summarize(row.en, 300);
  const summaryNe = summarize(row.ne, 300);
  const esc = (s) =>
    JSON.stringify(s)
      .replace(/\u202f/g, " ")
      .replace(/\u00a0/g, " ");
  lines.push(`  ${i}: { summary: ${esc(summary)}, summaryNe: ${esc(summaryNe)} },`);
}

lines.push("};", "");

const outPath = path.join(root, "src", "promiseSummaries.js");
fs.writeFileSync(outPath, lines.join("\n"), "utf8");
console.log("Wrote", outPath, "entries", fromApp.size);
